//Operadores aritméticos
x = 1
y = 2

soma = x + y
console.log(`Soma entre ${x} e ${y} é ${soma}\n`)

subtracao = x - y
console.log(`Subtração entre ${x} e ${y} é ${subtracao}\n`)

multiplicacao = x * y
console.log(`Multiplicação entre ${x} e ${y} é ${multiplicacao}\n`)

divisao = x / y
console.log(`Divisão entre ${x} e ${y} é ${divisao}\n`)

somaMaisIgual= soma += 10
console.log(`Soma += 10 é ${somaMaisIgual}`)
